@dp.callback_query(F.data.in_(["A", "B", "C"]))
async def handle_answer(callback: CallbackQuery):
    user_id = callback.from_user.id
    user_answers[user_id].append(callback.data)

    await callback.answer()

    if len(user_answers[user_id]) == 1:
        await callback.message.answer(QUESTION_2, reply_markup=answer_keyboard())

    elif len(user_answers[user_id]) == 2:
        await callback.message.answer(QUESTION_3, reply_markup=answer_keyboard())

    elif len(user_answers[user_id]) == 3:
        await send_result(callback.message, user_answers[user_id])

